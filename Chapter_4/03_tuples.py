# A tuple is immutable datatype in python that can store different datatypes.

a = (1,2,3,4,5)
print(type(a)) # Shows tuple in python

b = () # It is empty tuple.
print(type(b)) # Shows tuple in python

c = ("Om",) # For writing only 1 element, otherwise shows int,string etc if comma is not added
print(type(c)) # Shows tuple in python


